# Проверка числа на чётность
# Вариант 23

# Задаём число для проверки
number = 42  # Можно изменить на любое другое число

# Проверяем, чётное ли число
is_even = number % 2 == 0

# Выводим результат
print(f"Проверка числа на чётность:")
print(f"Заданное число: {number}")
print(f"Чётное? {is_even}")